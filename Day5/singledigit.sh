read -p "Enter the number:" Single
if [ $Single = 0 ]
then
	echo ZER0

elif [ $Single = 1 ]
then
        echo ONE

elif [ $Single = 2 ]
then
        echo TWO

elif [ $Single = 3 ]
then
        echo THREE

elif [ $Single = 4 ]
then
        echo FOUR

elif [ $Single = 5 ]
then
        echo FIVE

elif [ $Single = 6 ]
then
        echo SIX

elif [ $Single = 7 ]
then
        echo SEVEN

elif [ $Single = 8 ]
then
        echo EIGHT
else        
	echo NINE
fi


